public class CirclePra5 {
  private double x,y;
  private double r;

  public void setX(double x) {
    this.x = x;
  }

  public void setY(double y) {
    this.y = y;
  }

  public void setR(double r) {
    this.r = r;
  }

  public String toString() {
    return "圓心：(" + x + ", " + y + "),半徑：" + r;
  }


}


